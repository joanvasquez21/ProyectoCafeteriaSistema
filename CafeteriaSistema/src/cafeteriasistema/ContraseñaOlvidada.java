    public void validateEmail(){
        email = txtEmail.getText();
        if(email.matches(emailPattern)){ //Si email cumple con el patron emailPattern entonces el boton buscador se habilitara
            btnSearch.setEnabled(true);
        }else{
            btnSearch.setEnabled(false);
        }
    }
    public void validateFields(){
        String password = txtNewPasswor.getText();
        String answer = txtAnswer.getText();
        String securityQuestion = txtSecurityQuestion.getText();
        if(!password.equals("") && !answer.equals("") && !securityQuestion.equals("")){
            btnUpdate.setEnabled(true);
        }else{
            btnUpdate.setEnabled(false);
        }
    }

